    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtID.getText().equals("") || txtKasir.getText().equals("") || txtBarang.getText().equals("") 
            || txtJumlah.getText().equals("") || txtSatuan.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error",
            JOptionPane.WARNING_MESSAGE);
        } else {
            
            int hb;
            int jml;
            int tot;
            int diskon=10;
            int akhir;
            int max = 100000;
            
            String hkotor = txtSatuan.getText();
            hb = Integer.parseInt(hkotor);
            String jumlh = txtJumlah.getText();
            jml = Integer.parseInt(jumlh);
            
            tot = hb * jml;
            if (tot > max) {
                int semifnl = (tot * diskon / 100);
                akhir = tot - semifnl;
            }else{
                akhir = tot;
                diskon = 0;
            }
            
            
            String SQL = "INSERT INTO tabel_transaksi (id_barang, nama_kasir, nama_barang, jumlah_barang, harga_satuan, total_hb)"
                    + "VALUES('"+txtID.getText()
                    +"', '"+txtKasir.getText()
                    +"', '"+txtBarang.getText()
                    +"', '"+txtJumlah.getText()
                    +"', '"+txtSatuan.getText()
                    +"', '"+akhir+"')";
            String hargaTot = Integer.toString(akhir);
            txtTotal.setText(hargaTot);
            
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

