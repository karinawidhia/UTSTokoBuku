    private void selectData() {
        String kolom[] = {"IDBarang", "NamaKasir", "NamaBarang", "Jumlah", "HargaSatuan", "TotalHarga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tabel_transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while (rs.next()) {
                String ID = rs.getString(2);
                String NamaKasir = rs.getString(3);
                String NamaBarang = rs.getString(4);
                String Jumlah = rs.getString(5);
                String HargaSatuan = rs.getString(6);
                String Total = rs.getString(7);
                String data[] = {ID, NamaKasir, NamaBarang, Jumlah, HargaSatuan, Total};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmTambah.class).log(Level.SEVERE, null, ex);
        }
        tblOutput.setModel(dtm);
       //To change body of generated methods, choose Tools | Templates.
    }
    
    String nol_jam="";
    String nol_menit="";
    String nol_detik="";
    public void setJam() {
        ActionListener taskPerformer = new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                Date dt = new Date();
                int nilai_jam = dt.getHours();
                int nilai_menit = dt.getMinutes();
                int nilai_detik = dt.getSeconds();
                if (nilai_jam <= 9) {
                    nol_jam = "0";
                }
                if (nilai_menit <= 9) {
                    nol_menit = "0";
                }
                if (nilai_detik <= 9) {
                    nol_detik = "0";
                }
                
                String jam = nol_jam + Integer.toString(nilai_jam);
                String menit = nol_menit + Integer.toString(nilai_menit);
                String detik = nol_detik + Integer.toString(nilai_detik);
                labeljam.setText("Jam " + jam + ":" + menit + ":" + detik);
            }
        };
        new Timer(100, taskPerformer).start();
    }
